/***

Subtask-1:

Display sum of all the odd numbers from 91 to 129.

 */
let sumX = 0;
for (i = 91; i <= 129; i++) {
  if (i % 2 === 1) {
    sumX = sumX + i;
  }
}
console.log("sum of all the odd numbers from 91 to 129 is", sumX);

/***

Subtask-2:

Display sum of all the even numbers from 51 to 85.

 */
let sumY = 0;
for (j = 51; j <= 85; j++) {
  if (j % 2 === 0) {
    sumY = sumY + j;
  }
}
console.log("sum of all the even numbers from 51 to 85", sumY);
